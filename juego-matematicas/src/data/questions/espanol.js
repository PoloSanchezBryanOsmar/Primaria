  // Función para obtener preguntas por tema
  export const obtenerPreguntasPorTema = (tema, subtema = null, cantidad = 10) => {
    let preguntasFiltradas = preguntasEspanol.filter(pregunta => {
      let cumpleTema = pregunta.topic === tema;
      let cumpleSubtema = subtema ? pregunta.subtopic === subtema : true;
      
      return cumpleTema && cumpleSubtema;
    });
    
    // Mezclar preguntas
    preguntasFiltradas = preguntasFiltradas.sort(() => Math.random() - 0.5);
    
    // Retornar la cantidad solicitada
    return preguntasFiltradas.slice(0, cantidad);
  };
  
  // Función para obtener todos los temas disponibles
  export const obtenerTemasDisponibles = () => {
    const temas = {};
    
    preguntasEspanol.forEach(pregunta => {
      if (!temas[pregunta.topic]) {
        temas[pregunta.topic] = new Set();
      }
      temas[pregunta.topic].add(pregunta.subtopic);
    });
    
    // Convertir Sets a Arrays
    Object.keys(temas).forEach(tema => {
      temas[tema] = Array.from(temas[tema]);
    });
    
    return temas;
  };
  
  // Función para obtener preguntas mezcladas por múltiples temas
  export const obtenerPreguntasMixtas = (temas, cantidad = 10) => {
    let preguntasFiltradas = preguntasEspanol.filter(pregunta => {
      return temas.includes(pregunta.topic);
    });
    
    // Mezclar preguntas
    preguntasFiltradas = preguntasFiltradas.sort(() => Math.random() - 0.5);
    
    // Retornar la cantidad solicitada
    return preguntasFiltradas.slice(0, cantidad);
  };
  
  // Función para obtener todas las preguntas disponibles
  export const obtenerTodasLasPreguntas = () => {
    return [...preguntasEspanol];
  };
  
  // Función para obtener una pregunta por ID
  export const obtenerPreguntaPorId = (id) => {
    return preguntasEspanol.find(pregunta => pregunta.id === id);
  };
  
  // Función para contar preguntas por tema
  export const contarPreguntasPorTema = () => {
    const conteo = {};
    
    preguntasEspanol.forEach(pregunta => {
      if (!conteo[pregunta.topic]) {
        conteo[pregunta.topic] = 0;
      }
      conteo[pregunta.topic]++;
    });
    
    return conteo;
  };
  
